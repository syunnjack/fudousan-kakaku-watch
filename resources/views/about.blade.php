  <section class="mb-4">
    <h2 class="h5">家賃の参考情報について</h2>
    <p>
      不動産鑑定評価基準では、新規に賃料を求める手法として主に「積算法」「賃貸事例比較法」「収益分析法」の3つが挙げられています
      （継続賃料を求める際に用いる「差額配分法」等は対象外です）。当サイトでは、このうち以下の2つを組み合わせて家賃の参考情報を提供しています。
    </p>
