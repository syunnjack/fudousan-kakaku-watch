        不動産情報ライブラリの取引価格㎡単価を基礎価格の近似値として、想定期待利回り（年5.0%）で試算した概算値です。
        公租公課・損害保険料・維持修繕費・空室損失相当額などの必要諸経費は含んでいないため、実際の適正賃料より低めに出る傾向があります。
      </li>
      <li>
        <strong>賃貸事例比較法（実測値）</strong>：「家賃口コミ」ページでは、実際にその地域に住んでいる方から投稿していただいた家賃事例を集計・一覧表示しています。
        投稿内容は利用者からの申告であり、運営による事実確認は行っていません。
      </li>
    </ul>
